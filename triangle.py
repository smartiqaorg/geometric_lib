def area(a, h):
	''' Получаем два числа - длину оснований и висоту треугольника. По формуле площади треугольника находим его площадь''' 
	return a * h / 2
def perimeter(a, b, c):
	''' Получаем три числа - длину всех сторон треугольника. По формуле периметра треугольника находим его периметр''' 
	return a + b + c